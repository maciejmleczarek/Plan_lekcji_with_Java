        frame.add(new JScrollPane(table), BorderLayout.CENTER);
        for (int godz = 0; godz < 11; godz++) {
            for (int dzien = 1; dzien < 6; dzien++) {
                table.setValueAt("", godz, dzien);
            }
        }
        for (int p = 0; p < this.generowanyPlan.getPlan().size(); p++) {
            ourPlan = (Plan) this.generowanyPlan.getPlan().get(p);
            if ((ourPlan.getOddzial1().equals(title) )) {
                for (int g = 0; g < 11; g++) {
                    if (ourPlan.getGodzina() == g) {
                        for (int d = 1; d < 6; d++) {
                            if (ourPlan.getDzien() == d) {
                                table.setValueAt(ourPlan.getZawartosc() + " ", g, d);

                            }
                        }
                    }
                }
            }
        }
    //   System.out.println("Wielkosc karteczek " + this.generowanyPlan.getPlan().size());
