    private void changeHourOfLanguage(int yearNumber, String number) {
        SchoolGroup ourGroup;
        List<SchoolSubject> ourSubjects;
        List<SchoolSubject> newSubjects;
        Language ourLanguage;
        boolean dodanyJezyk;
        for (int i = 0; i < this.school.getSchoolGroup().size(); i++) {
            ourGroup = (SchoolGroup) this.school.getSchoolGroup().get(i);
            if (ourGroup.getYearName() == yearNumber) {
                ourSubjects = ourGroup.getSubjectGroupList();
                newSubjects = new ArrayList<SchoolSubject>();
                for (int j = 0; j < ourSubjects.size(); j++) {
                    dodanyJezyk = false;
                    newSubjects.add(ourSubjects.get(j));
                    for (int n = 0; n < this.school.getLanguage().size(); n++) {
                        ourLanguage = (Language) this.school.getLanguage().get(n);
                        if (ourSubjects.get(j).getName().equals(ourLanguage.getLanguageName())) {
                            newSubjects.remove(ourSubjects.get(j));
                            newSubjects.add(new SchoolSubject(ourLanguage.getLanguageName(), Integer.parseInt(number), ourLanguage.getLanguageName()));
                            dodanyJezyk = true;
                        }
                    }
                //      if (dodanyJezyk=false) newSubjects.add(ourSubjects.get(j));

                }
                ourGroup.setSubjectGroupList(newSubjects);
            }
        }
        Lesson ourLekcja;
        for (int i = 0; i < this.school.getLesson().size(); i++) {
            ourLekcja = (Lesson) this.school.getLesson().get(i);
            if (ourLekcja.getNameYear() == yearNumber) {
                for (int j = 0; j < this.school.getLanguage().size(); j++) {
                    ourLanguage = (Language) this.school.getLanguage().get(j);
                    if (ourLekcja.getPrzedmiot().equals(ourLanguage.getLanguageName())) {
                        ourLekcja.setIloscGodzin(Integer.parseInt(number));
                    }
                }
            }
        }
    }

