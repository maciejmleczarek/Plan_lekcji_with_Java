private void dodanieLekcji(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dodanieLekcji
    Lesson ourL;
    if (!jList7.isSelectionEmpty() && !jList8.isSelectionEmpty() && !jList9.isSelectionEmpty()) {

        String oddzial = (String) jList7.getSelectedValue();
        String przedmiot = (String) jList8.getSelectedValue();
        String nauczyciel = (String) jList9.getSelectedValue();
        boolean obecnoscLekcji = this.school.sprawdzLekcje(oddzial, przedmiot);
        ustawieniePoprawnosciGrup();
        SchoolGroup ourGroup = null;
        SchoolSubject ourSubject;
        int rocznik = 0;
        int iloscGodzin = 0;
        for (int i = 0; i < this.school.getSchoolGroup().size(); i++) {
            ourGroup = (SchoolGroup) this.school.getSchoolGroup().get(i);
            if (ourGroup.getCompleteGroupName().equals(oddzial)) {
                for (int j = 0; j < ourGroup.getSubjectGroupList().size(); j++) {
                    ourSubject = (SchoolSubject) ourGroup.getSubjectGroupList().get(j);
                    if (ourSubject.getName().equals(przedmiot)) {
                        iloscGodzin = ourSubject.getHourOfSubject();
                        rocznik = ourGroup.getYearofGroup();
                    }
                }
            }
        }
        if (!obecnoscLekcji) {
            System.out.println("Dodaje lekcje");
            this.school.addLesson(new Lesson(rocznik, (String) jList7.getSelectedValue(), (String) jList8.getSelectedValue(), (String) jList9.getSelectedValue(), iloscGodzin));
            System.out.println("ilośc godzin = " + iloscGodzin);
            ListModel lista;
            Object element;
            int numer = 0;
            PoprawnoscPrzedmiotow poprawnoscPrzedmiotu;
            poprawnoscPrzedmiotu = this.school.getPoprawnoscPrzedmiotowByName(oddzial);
            lista = jList8.getModel();
            for (int i = 0; i < lista.getSize(); i++) {
                element = lista.getElementAt(i);
                if (przedmiot.equals((String) element)) {
                    numer = i;
                }
            }
            poprawnoscPrzedmiotu.changePoprawnoscPrzedmiotu(numer, "+");
            jList10.setListData(this.school.getListaPoprawnosciPrzedmiotow(oddzial));
            ustawieniePoprawnosciGrup();
        } else {
            Lesson lesson = this.school.getLessonByGroupAndSubject(oddzial, przedmiot);
            lesson.setNauczyciel(nauczyciel);
            lesson.setIloscGodzin(iloscGodzin);
            System.out.println(lesson.getIloscGodzin());
        }
    }
    for (int i = 0; i < this.school.getLesson().size(); i++) {
        ourL = (Lesson) this.school.getLesson().get(i);
        System.out.println("Rocznik " + ourL.getNameYear() + " " + ourL.getOddzial() + " " + ourL.getPrzedmiot() + " " + ourL.getNauczyciel() + " " + ourL.getIloscGodzin());
    }
}//GEN-LAST:event_dodanieLekcji
    public void ustawieniePoprawnosciGrup() {
        boolean grupaPoprawna = true;

        int iloscGrup = this.school.getSchoolGroup().size();
        String[] poprawnisciGrup = new String[iloscGrup];
        int iloscPrzedmiotow = 0;
        SchoolGroup grupa = null;
        String competeNazwa;
        String[] popPrzedmiotow;
        for (int i = 0; i < iloscGrup; i++) {
            grupa = (SchoolGroup) this.school.getSchoolGroup().get(i);
            competeNazwa = grupa.getCompleteGroupName();
            iloscPrzedmiotow = grupa.getSubjectGroupList().size();
            PoprawnoscPrzedmiotow przedmioty = this.school.getPoprawnoscPrzedmiotowByName(competeNazwa);
            popPrzedmiotow = przedmioty.getPoprawnosc();
            poprawnisciGrup[i] = "+";
            System.out.println("Rocznik " + grupa.getYearName() + competeNazwa + " ma " + popPrzedmiotow.length + " przedmiotow");
            for (int j = 0; j < popPrzedmiotow.length; j++) {
                System.out.print(popPrzedmiotow[j]);
                if (popPrzedmiotow[j].equals("-")) {
                    poprawnisciGrup[i] = "-";
                }

            }


        }
        this.school.setListPoprawnosciOddzialow(poprawnisciGrup);
        jList11.setListData(this.school.getPoprawnoscOddzialow().getPoprawnosc());
        System.out.println(" Ilosc lekcji = " + this.school.getLesson().size());
    }

    private void zaznaczenieNauczyciela() {
        ListModel lista;
        Object element;
        int numer = 0;
        if (!jList7.isSelectionEmpty() && !jList8.isSelectionEmpty()) {
            String oddzial = (String) jList7.getSelectedValue();
            String przedmiot = (String) jList8.getSelectedValue();
            String nauczyciel = null;
            boolean obecnoscObiektu = this.school.sprawdzLekcje(oddzial, przedmiot);

            if (obecnoscObiektu) {
                nauczyciel = this.school.getTeacherByGroupAndSubject(oddzial, przedmiot);
                lista = jList9.getModel();
                for (int i = 0; i < lista.getSize(); i++) {
                    element = lista.getElementAt(i);
                    if (nauczyciel.equals((String) element)) {
                        numer = i;
                    }
                }
                jList9.setSelectedIndex(numer);
            }
        }
    }

